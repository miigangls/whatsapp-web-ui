function makeMessages(chatId: string, pairs: Array<{ from: 'me' | string; content: string; hoursAgo: number }>): Message[] {
  return pairs.map((p, i) => {
    const ts = new Date(Date.now() - p.hoursAgo * 3600000 - i * 60000);
    return {
      id: `${chatId}-msg-${i}`,
      chatId,
      content: p.content,
      timestamp: ts.toISOString(),
      senderId: p.from === 'me' ? CURRENT_USER.id : p.from,
      status: p.from === 'me' ? 'read' : 'delivered',
      type: 'text',
    };
  });
}

